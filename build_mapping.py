# --- CSV (включая исключённые — для прозрачности решения) ---
rows = []
for code in sorted(cnt, key=lambda c: (M[c][1], -cnt[c])):
    desc, grp = M[code]
    rows.append((grp, code, cnt[code], desc))
dropped_rows = sum(cnt[c] for c in cnt if M[c][1] == DROP)
with open('code_group_mapping_v1.csv', 'w', encoding='utf-8-sig', newline='') as f:
    w = csv.writer(f)
    w.writerow(['group', 'code', 'count', 'description'])
    w.writerows(rows)

# --- сводка по группам (без DROP) ---
gcnt = Counter()
for c in cnt:
    if M[c][1] != DROP: gcnt[M[c][1]] += cnt[c]
print("=== ИТОГОВЫЕ ГРУППЫ ===")
